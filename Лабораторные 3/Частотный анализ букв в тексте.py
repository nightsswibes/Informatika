def count_letters(text):  #функция подсчета букв
    letters = {}  #создаем пустой словарь

    for symbol in text.lower():  #приводим текст к нижнему регистру
        if symbol.isalpha():  #проверяем что это буква
            if symbol in letters:  #если буква уже есть в словаре
                letters[symbol] += 1  #увеличиваем счетчик
            else:
                letters[symbol] = 1  #добавляем новую букву

    return letters  #возвращаем словарь с количеством


def calculate_frequency(letters):  #функция расчета частоты букв
    freq = {}  #создаем словарь для частот
    total = 0  #переменная для общего количества букв

    for value in letters.values():  #перебираем все значения словаря
        total += value  #считаем общее количество букв

    for letter in letters:  #перебираем буквы
        freq[letter] = letters[letter] / total  #считаем частоту буквы

    return freq  #возвращаем словарь частот


